from PIL import Image

img_file = Image.open("test_exp5/ams_2004_white.png")
img = img_file.load()

xs, ys = img_file.size

green = 0

for x in range(0, xs):
    for y in range(0, ys):
      _, g, _ = img[x, y]
    #   print(g)
      green += (255- g)

green_density = green/ (xs * ys)
print(green, green_density)

